const I18N_STORAGE_KEY = 'devsim_lang';

/** Игровые суммы в API хранятся в ₽; для EN показываем USD. */
const RUB_PER_USD = 100;

let currentLang = localStorage.getItem(I18N_STORAGE_KEY) || 'ru';

function moneyCurrency() {
    return currentLang === 'en' ? 'USD' : 'RUB';
}

function formatMoney(amountRub) {
    const rub = Math.round(Number(amountRub) || 0);
    if (moneyCurrency() === 'USD') {
        const usd = Math.round(rub / RUB_PER_USD);
        return '$' + usd.toLocaleString('en-US');
    }
    return rub.toLocaleString('ru-RU') + '\u00a0₽';
}

function formatSignedMoney(amountRub) {
    const rub = Math.round(Number(amountRub) || 0);
    if (moneyCurrency() === 'USD') {
        const usd = Math.round(Math.abs(rub) / RUB_PER_USD);
        const formatted = '$' + usd.toLocaleString('en-US');
        if (rub > 0) return '+' + formatted;
        if (rub < 0) return '−' + formatted;
        return formatted;
    }
    const num = Math.abs(rub).toLocaleString('ru-RU');
    if (rub > 0) return '+' + num + '\u00a0₽';
    if (rub < 0) return '−' + num + '\u00a0₽';
    return num + '\u00a0₽';
}

function localizeMoneyInText(text) {
    if (!text || moneyCurrency() !== 'USD') return text;
    return String(text).replace(/([+\u2212-]?)\s*([\d\u00a0\s]+)\s*₽/g, (full, sign, numStr) => {
        const rub = parseInt(numStr.replace(/[\s\u00a0]/g, ''), 10);
        if (Number.isNaN(rub)) return full;
        const signed = sign === '−' || sign === '-' ? -rub : sign === '+' ? rub : rub;
        return formatSignedMoney(signed);
    });
}

function formatCareerTierShort(years) {
    const y = Number(years) || 0;
    if (y === 0) return 'Intern';
    if (y <= 2) return 'Junior';
    if (y <= 5) return 'Middle';
    return 'Senior';
}

function formatPayGrade(workspace) {
    if (!workspace) return '';
    if (moneyCurrency() !== 'USD') return workspace.payGrade || '';
    const p = workspace.player;
    const monthly = workspace.estimatedMonthlyGross ?? 0;
    return `${formatCareerTierShort(p?.experienceYears)} · Level ${p?.level ?? 1} · ~${formatMoney(monthly)}/mo`;
}

function formatSalaryReviewLabel(backendLabel, day) {
    if (moneyCurrency() !== 'USD') return backendLabel || '—';
    const m = String(backendLabel || '').match(/(\d+)/);
    const n = m ? parseInt(m[1], 10) : Math.max(1, 30 - ((day || 1) % 30));
    return t('portal.pay.reviewIn', { n });
}

function t(key, vars = {}) {
    const table = I18N_STRINGS[currentLang] || I18N_STRINGS.ru;
    let text = table[key] ?? I18N_STRINGS.ru[key] ?? key;
    Object.entries(vars).forEach(([k, v]) => {
        text = text.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    });
    return text;
}

function getLang() {
    return currentLang;
}

function setLang(lang) {
    if (!I18N_STRINGS[lang]) return;
    currentLang = lang;
    localStorage.setItem(I18N_STORAGE_KEY, lang);
    document.documentElement.lang = lang === 'en' ? 'en' : 'ru';
    document.title = t('app.title');
    applyDomTranslations();
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });
    if (typeof onLangChange === 'function') onLangChange();
}

function applyDomTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const attr = el.getAttribute('data-i18n-attr');
        const text = t(key);
        if (attr) el.setAttribute(attr, text);
        else el.textContent = text;
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
    });
}

function initLangSwitch() {
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => setLang(btn.dataset.lang));
    });
    setLang(currentLang);
}

